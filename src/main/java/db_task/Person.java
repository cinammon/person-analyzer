package db_task;

public class Person {

    private String name;
    private Integer age;
    private Integer carsNo;

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public Integer getAge() {
        return age;
    }

    public void setAge(Integer age) {
        this.age = age;
    }

    public Integer getCarsNo() {
        return carsNo;
    }

    public void setCarsNo(Integer carsNo) {
        this.carsNo = carsNo;
    }
}
